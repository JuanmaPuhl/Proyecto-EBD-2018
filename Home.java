    private void checkSelected(){
        if(selected_home)
            seleccionar(0);
        if(selected_empleado)
            seleccionar(3);
        if(selected_admin)
            seleccionar(1);
        if(selected_atm)
            seleccionar(2);
        
    }
    
    private void seleccionar(int i){
        if(i==0){ //Era home
            selected_home=true;
            selected_admin=false;
            selected_atm=false;
            selected_empleado=false;
            setColor(btn_home);
            resetColor(btn_admin);
            resetColor(btn_atm);
            resetColor(btn_empleado);
            contentVisible(contentHome,true);
            contentVisible(panelAdmin,false);
            contentVisible(panelAtm,false);
            contentVisible(panelEmpleado,false);
        }
        if(i==1){ //Era admin
            selected_home=false;
            selected_admin=true;
            selected_atm=false;
            selected_empleado=false;
            setColor(btn_admin);
            resetColor(btn_empleado);
            resetColor(btn_atm);
            resetColor(btn_home);
        }
        if(i==2){ //Era atm
            selected_home=false;
            selected_admin=false;
            selected_atm=true;
            selected_empleado=false;
            setColor(btn_atm);
            resetColor(btn_admin);
            resetColor(btn_empleado);
            resetColor(btn_home);
        }
        if(i==3){ //Era empleado
            selected_home=false;
            selected_admin=false;
            selected_atm=false;
            selected_empleado=true;
            setColor(btn_empleado);
            resetColor(btn_admin);
            resetColor(btn_atm);
            resetColor(btn_home);
        }
    }

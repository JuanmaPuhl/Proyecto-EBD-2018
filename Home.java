    /**Evento al presionar btn_ingresarTarjeta*/
    private void btn_ingresarTarjetaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ingresarTarjetaMousePressed
        try
        {
            int nro_cuenta=-1;
            int nro_tarjeta=Integer.parseInt(tfield_tarjeta.getText());
            this.conectarBD("atm","atm");
            java.sql.Statement stmt = c.createStatement();
            java.sql.ResultSet rs = stmt.executeQuery("Select nro_ca from tarjeta where nro_tarjeta="+nro_tarjeta+" AND PIN LIKE '"+this.cryptMD5(this.tfield_pin.getText())+"'");
            while(rs.next())
                nro_cuenta=rs.getInt("nro_ca");
            stmt.close();
            rs.close();
            this.desconectarBD();
            if(nro_cuenta==-1)
                this.lbl_ErrorLoginTarjeta.setVisible(true);
            else
            {
                this.panelAtm.setTarjeta(nro_tarjeta);
                this.panelAtm.setNro(nro_cuenta);
                aclarar();
                contentVisible(contentHome,false);
                contentVisible(panelAdmin,false);
                contentVisible(panelAtm,true);
                contentVisible(panelEmpleado,false);
                lbl_seccion.setText("Seccion Tarjeta");
                lbl_mensaje.setText("Bienvenido a la seccion Tarjeta.      Cuenta actual: "+nro_cuenta+".");
                tfield_tarjeta.setText("");
                tfield_pin.setText("");
                this.loginTarjeta.setVisible(false);
                seleccionar(2);
            }
        } catch (SQLException ex) {
            this.lbl_ErrorLoginTarjeta.setVisible(true);
            
        }
        catch(NumberFormatException ex)
        {
            //Error al leer datos.
            JOptionPane.showMessageDialog(this,
                                           "El numero de tarjeta no es valido" ,
                                           "Error",
                                           JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_ingresarTarjetaMousePressed

